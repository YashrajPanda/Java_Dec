// Literal in java

class Literal {

    public static void main(String args[]) {
        // Integer Literal
        int intLiteral = 100;
        System.out.println("Integer Literal: " + intLiteral);

        // Floating Point Literal
        float floatLiteral = 10.5f;
        System.out.println("Floating Point Literal: " + floatLiteral);

        // Character Literal
        char charLiteral = 'A';
        System.out.println("Character Literal: " + charLiteral);

        // String Literal
        String stringLiteral = "Hello, Java!";
        System.out.println("String Literal: " + stringLiteral);

        // Boolean Literal
        boolean boolLiteral = true;
        System.out.println("Boolean Literal: " + boolLiteral);

        // Null Literal
        String nullLiteral = null;
        System.out.println("Null Literal: " + nullLiteral);
    }
}