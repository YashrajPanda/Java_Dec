// Varibales in Java
class Variable {
    public static void main(String[] args) {
        // Declare an integer variable
        int myNumber = 10;
        
        // Declare a string variable
        String myText = "Hello, Java!";
        
        // Print the variables
        System.out.println("Integer: " + myNumber);
        System.out.println("String: " + myText);
    }
}

